package Programs_IQ.Swapping_Integers;

public class Swap_WithOut_temp {

    public static void main(String[] args) {


        int x = 5;
        int y = 10;


        //swapping using third var
        int t;

        t = x;//5
        x = y;//10
        y = t;//10


        System.out.println(x);
        System.out.println(y);
        System.out.println(t);



    }
}
